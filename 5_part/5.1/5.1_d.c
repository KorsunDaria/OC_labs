// Принимает виртуальный адрес, возвращает номер физической страницы (PFN)
uintptr_t get_page(uintptr_t virtual_addr) {

    // Открываем /proc/self/pagemap — специальный файл ядра.
    // Для каждой виртуальной страницы там хранится 8-байтовая запись с PFN и флагами.
    // O_RDONLY — только чтение, писать туда нельзя.
    int fd = open("/proc/self/pagemap", O_RDONLY);

    if (fd < 0) {
        // Если не удалось открыть — скорее всего нет прав (нужен sudo или CAP_SYS_ADMIN)
        perror("open pagemap (run with sudo if needed)");
        return 0;
    }

    uint64_t value;  // сюда прочитаем 8-байтовую запись из pagemap

    // sysconf(_SC_PAGESIZE) возвращает размер страницы — обычно 4096 байт (4 КБ)
    long page_size = sysconf(_SC_PAGESIZE);

    // Вычисляем смещение внутри файла pagemap.
    // Логика: pagemap — это массив 8-байтовых записей, по одной на каждую страницу.
    // Номер страницы = virtual_addr / page_size (целочисленное деление, отбрасывает остаток)
    // Смещение в файле = номер_страницы * 8 (sizeof(uint64_t))
    // Пример: addr=0x601020, page_size=4096 → страница №0x601 → offset=0x601*8
    off_t offset = (virtual_addr / page_size) * sizeof(value);

    // pread — читает из файла по конкретному смещению, не меняя позицию файлового дескриптора.
    // Аргументы: fd, куда_читать, сколько_байт, с_какого_смещения
    // Если прочитали не 8 байт — что-то пошло не так (страница не замаплена и т.д.)
    if (pread(fd, &value, sizeof(value), offset) != sizeof(value)) {
        close(fd);
        return 0;
    }

    close(fd);  // всегда закрываем дескриптор, иначе утечка fd

    // Проверяем бит 63 записи pagemap — он означает "страница сейчас в RAM"
    // (1ULL << 63) создаёт маску: 1000...000 (63 нуля)
    // Если бит 63 == 0 — страница выгружена в swap или ещё не аллоцирована → PFN недоступен
    if (!(value & (1ULL << 63))) {
        return 0;
    }

    // Извлекаем PFN из битов 0–54.
    // (1ULL << 55) - 1 создаёт маску: 0000...0111...1 (55 единиц)
    // Побитовое AND оставляет только нижние 55 бит — это и есть номер физической страницы.
    // Биты 55–62 — флаги (soft-dirty, exclusive mapped и т.д.), они нам не нужны.
    uint64_t pfn = value & ((1ULL << 55) - 1);

    return pfn;
}


// Печатает виртуальные адреса, физические PFN и значения двух переменных.
// label — строка-заголовок ("До fork", "После записи" и т.д.)
// g — указатель на global_var, l — указатель на local_var
void print_ptrs(const char* label, int* g, int* l) {

    printf("--- %s ---\n", label);

    // Приводим указатели к uintptr_t (целое число = числовой адрес)
    // и передаём в get_page, чтобы узнать физические номера страниц
    uintptr_t phys_g = get_page((uintptr_t)g);
    uintptr_t phys_l = get_page((uintptr_t)l);

    // %p — формат для указателей, печатает виртуальный адрес в hex
    printf("Virt: G=%p, L=%p\n", (void*)g, (void*)l);

    // %lx — unsigned long в hex. Это и есть PFN — номер физической страницы.
    // Если до COW: у родителя и дочернего эти числа совпадут.
    // После записи в дочернем: PFN у дочернего изменится — ядро создало копию страницы.
    printf("Phys PFN: G=0x%lx, L=0x%lx\n", phys_g, phys_l);

    // *g и *l — разыменовываем указатели, получаем сами значения переменных
    printf("Val : G=%d, L=%d\n", *g, *l);

    printf("\n");
}


int main() {

    int local_var = 10;  // локальная переменная — живёт на стеке main()
    pid_t pid;           // тип для хранения PID процесса

    // Печатаем стартовое состояние — одна страница, один процесс
    print_ptrs("До fork()", &global_var, &local_var);

    // fork() — системный вызов, создаёт дочерний процесс.
    // Возвращает разные значения в каждом процессе:
    //   в родителе → PID дочернего (положительное число)
    //   в дочернем → 0
    //   при ошибке → -1 (только в родителе, дочернего нет)
    pid = fork();

    if (pid < 0) {
        // fork не удался — например, превышен лимит процессов (ulimit -u)
        perror("fork failed");
        exit(1);
    }

    else if (pid == 0) {
        // ── ДОЧЕРНИЙ ПРОЦЕСС ──
        // pid == 0 — это условие истинно только в дочернем.
        // getpid() возвращает PID текущего процесса.

        printf("--- В дочернем процессе (PID: %d) ---\n", getpid());

        // Первый print: сразу после fork, до любых записей.
        // COW ещё не срабатывал — дочерний только читает страницы.
        // PFN здесь будет совпадать с родительским (та же физ. страница).
        print_ptrs("Сразу после fork (чтение)", &global_var, &local_var);

        printf("Изменяем переменные в дочернем процессе...\n\n");

        // ЗАПИСЬ в переменную → ядро получает page fault (страница помечена read-only для COW).
        // Ядро создаёт физическую копию страницы, дочернему выдаётся новая страница.
        // Родитель продолжает видеть оригинальную страницу со старым значением.
        global_var = 200;
        local_var  = 20;

        // Второй print: после записи.
        // PFN у дочернего теперь ДРУГОЙ — это новая физ. страница (копия).
        // Виртуальный адрес тот же, что был! Только физика изменилась.
        print_ptrs("После изменения (запись)", &global_var, &local_var);

        // exit(5) — завершаем дочерний с кодом 5.
        // Этот код родитель поймает через waitpid → WEXITSTATUS.
        exit(5);
    }

    else {
        // ── РОДИТЕЛЬСКИЙ ПРОЦЕСС ──
        // pid > 0 — это PID дочернего процесса

        // Ждём 2 секунды — грубый способ дать дочернему время отработать и записать переменные.
        // В реальном коде используют pipe/сигналы для синхронизации.
        sleep(2);

        printf("--- В родительском процессе (PID: %d) ---\n", getpid());

        // Родитель убеждается что его данные не изменились:
        // global_var == 100, local_var == 10, PFN те же что до fork.
        // COW гарантирует изоляцию — дочерний менял свою копию, не нашу.
        print_ptrs("После того как ребенок изменил свои копии", &global_var, &local_var);

        int status;  // сюда waitpid запишет информацию о завершении дочернего

        // waitpid(pid, &status, 0) — блокируется, пока процесс pid не завершится.
        // Без этого вызова дочерний стал бы зомби-процессом:
        // он завершился, но его запись в таблице процессов осталась бы — родитель
        // не "принял" код завершения.
        waitpid(pid, &status, 0);

        // WIFEXITED(status) — макрос, проверяет что процесс завершился через exit() (не сигналом).
        // WEXITSTATUS(status) — извлекает код: exit(5) → напечатает 5.
        if (WIFEXITED(status)) {
            printf("\nДочерний процесс завершен. Код: %d\n", WEXITSTATUS(status));
        }
    }

    return 0;
}