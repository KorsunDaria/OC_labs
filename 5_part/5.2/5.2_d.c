#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <sys/wait.h>

int main() {
    // Создаем новый процесс с помощью системного вызова fork().
    // Он дублирует вызывающий процесс.
    pid_t pid = fork();

    if (pid < 0) {
        // Если fork() возвращает значение меньше 0, произошла ошибка создания процесса.
        perror("fork failed");
        exit(1);
    } 
    else if (pid == 0) {
        // Этот блок выполняется только в дочернем процессе (так как pid == 0).
        printf("Ребенок (PID: %d) завершается и становится ЗОМБИ.\n", getpid());
        
        // Дочерний процесс вызывает exit(0), что приводит к его завершению.
        // ОС освобождает его память и ресурсы, но сохраняет структуру PCB 
        // (Process Control Block) в таблице процессов, чтобы родитель мог 
        // считать код возврата. Процесс переходит в состояние "зомби".
        exit(0); 
    } 
    else {
        // Этот блок выполняется только в родительском процессе.
        // Переменная pid содержит PID дочернего процесса.
        printf("Родитель (PID: %d) засыпает на 60 секунд!\n", getpid());
        
        // Родительский процесс переходит в спящий режим на 60 секунд.
        // В течение этого времени родитель НЕ вызывает функцию wait() или waitpid(),
        // поэтому дочерний процесс остается в состоянии зомби.
        sleep(60); 
        
        // После завершения 60-секундного сна родительский процесс продолжает выполнение
        // и завершается, что приводит к завершению его жизненного цикла.
    }

    return 0;
}


#include <stdio.h>
#include <stdlib.h>
#include <sys/wait.h>
#include <unistd.h>

int main() {
  // Вызов fork() создает точную копию текущего процесса (дочерний процесс).
  // Он возвращает разные значения для родителя и для ребенка.
  pid_t pid = fork();

  if (pid < 0) {
    // Если fork() вернул отрицательное число, значит произошла ошибка
    // (например, достигнут лимит процессов в ОС).
    perror("fork failed");
    exit(1);
  } else if (pid > 0) {
    // Этот блок выполняется ТОЛЬКО в родительском процессе.
    // pid здесь содержит идентификатор созданного ребенка.
    printf("Родитель (PID: %d) создал ребенка и завершается!\n", getpid());

    // Родитель завершает свою работу ДО того, как завершится ребенок.
    // Он НЕ вызывает функцию wait(), поэтому дочерний процесс станет "сиротой".
    exit(0);
  } else {
    // Этот блок выполняется ТОЛЬКО в дочернем процессе (здесь pid == 0).
    // getpid() - ID самого ребенка, getppid() - ID его родителя.
    printf("Ребенок (PID: %d). Мой изначальный родитель (PPID): %d\n", getpid(),
           getppid());

    // Ребенок засыпает на 2 секунды. За это время родительский процесс
    // гарантированно успеет выполнить exit(0) и умереть.
    sleep(2);

    printf("\nРебенок (PID: %d) проснулся.\n", getpid());

    // Так как настоящий родитель мертв, ОС (ядро Linux) автоматически
    // переназначает родителем этого процесса процесс init (PID 1) или systemd.
    // getppid() теперь вернет другой ID (обычно 1 или ID пользовательского
    // менеджера сеансов).
    printf("Родитель мертв. Мой НОВЫЙ усыновитель (PPID): %d\n", getppid());

    // Имитация полезной работы, чтобы процесс пожил, и его можно было увидеть в
    // htop/ps.
    sleep(30);
  }
  return 0;
}