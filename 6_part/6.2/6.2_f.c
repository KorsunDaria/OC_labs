#include <stdio.h>  // Стандартная библиотека ввода/вывода: printf, scanf, perror, fflush
#include <stdlib.h>  // Стандартная библиотека: exit(), EXIT_FAILURE, EXIT_SUCCESS
#include <sys/wait.h>  // Макросы и функция wait() для ожидания завершения дочернего процесса
#include <unistd.h>  // POSIX API: pipe(), fork(), dup2(), close(), STDIN_FILENO, STDOUT_FILENO, sleep()

// ─────────────────────────────────────────────────────────────────────────────
// Функция run_child() — выполняется в дочернем процессе.
// Читает числа из своего stdin (который будет перенаправлен в pipe)
// и печатает каждое прочитанное число. Работает до EOF.
// Синхронная — выполняется последовательно, блокирует процесс на scanf.
// ─────────────────────────────────────────────────────────────────────────────
void run_child() {
  unsigned int received;  // Переменная для хранения одного считанного числа
                          // (беззнаковое целое)

  while (scanf("%u", &received) !=
         EOF) {  // Цикл: читаем из stdin одно беззнаковое целое число;
                 // scanf возвращает EOF когда поток закрыт (родитель закрыл
                 // свой конец pipe)
    printf("[Дочерний процесс] Получено число: %u\n",
           received);  // Выводим полученное число в stdout дочернего процесса
  }

  printf("[Дочерний процесс] Поток завершен.\n");  // Сообщаем о завершении —
                                                   // получен EOF, больше данных
                                                   // не будет
}

// ─────────────────────────────────────────────────────────────────────────────
// Функция spawn_and_redirect() — создаёт pipe, форкает процесс,
// перенаправляет stdin/stdout через pipe и организует обмен данными.
// Содержит как синхронные вызовы (pipe, fork), так и блокирующие (wait).
// ─────────────────────────────────────────────────────────────────────────────
void spawn_and_redirect() {
  int pipefd[2];  // Массив из двух файловых дескрипторов:
                  //   pipefd[0] — конец для чтения (read end)
                  //   pipefd[1] — конец для записи (write end)

  if (pipe(pipefd) ==
      -1) {  // pipe() — СИНХРОННЫЙ системный вызов; создаёт однонаправленный
             // канал (pipe) в ядре; возвращает -1 при ошибке
    perror("pipe");  // perror() — выводит системное сообщение об ошибке с
                     // префиксом "pipe"
    exit(EXIT_FAILURE);  // EXIT_FAILURE = 1; завершаем программу с кодом ошибки
  }

  pid_t pid = fork();  // fork() — СИСТЕМНЫЙ ВЫЗОВ; создаёт точную копию
                       // текущего процесса (дочерний процесс); pid_t — тип для
                       // хранения идентификатора процесса; возвращает: 0 — в
                       // дочернем, PID дочернего — в родительском, -1 — ошибка

  if (pid == -1) {   // Проверяем, не произошла ли ошибка при fork()
    perror("fork");  // Выводим системное сообщение об ошибке с префиксом "fork"
    exit(EXIT_FAILURE);  // Завершаем программу с кодом ошибки
  }

  // ── ДОЧЕРНИЙ ПРОЦЕСС (pid == 0) ──────────────────────────────────────────
  if (pid ==
      0) {  // Этот блок выполняется только в дочернем процессе (fork вернул 0)

    close(pipefd[1]);  // Закрываем write-конец pipe в дочернем — дочерний
                       // только читает, не пишет; важно закрыть, иначе EOF
                       // никогда не придёт

    dup2(pipefd[0],
         STDIN_FILENO);  // dup2(oldfd, newfd) — СИНХРОННЫЙ вызов; дублирует
                         // дескриптор pipefd[0] на место STDIN_FILENO (=0);
                         // теперь stdin дочернего == read-конец pipe

    close(pipefd[0]);  // Закрываем оригинальный дескриптор pipefd[0] — он уже
                       // продублирован в stdin, держать второй экземпляр нет
                       // смысла

    run_child();  // Запускаем логику дочернего процесса: читаем числа из stdin
                  // (= pipe) и печатаем их

    exit(EXIT_SUCCESS);  // Завершаем дочерний процесс с кодом успеха (0) после
                         // того как run_child вернётся

    // ── РОДИТЕЛЬСКИЙ ПРОЦЕСС (pid > 0) ───────────────────────────────────────
  } else {  // Этот блок выполняется только в родительском процессе (fork вернул
            // PID дочернего)

    close(pipefd[0]);  // Закрываем read-конец pipe в родительском — родитель
                       // только пишет, не читает

    dup2(pipefd[1],
         STDOUT_FILENO);  // dup2() — перенаправляем stdout родителя
                          // (STDOUT_FILENO=1) на write-конец pipe; теперь
                          // printf родителя будет писать прямо в pipe

    close(pipefd[1]);  // Закрываем оригинальный write-дескриптор — он уже
                       // продублирован в stdout

    unsigned int numbers[] = {10, 20, 30, 40,
                              50};  // Массив из 5 беззнаковых целых чисел для
                                    // отправки дочернему процессу

    for (int i = 0; i < 5; i++) {  // Цикл по всем 5 элементам массива

      printf("%u\n", numbers[i]);  // printf() пишет число в stdout, который
                                   // теперь == pipe; дочерний процесс получит
                                   // это число через свой stdin

      fflush(
          stdout);  // fflush() — СИНХРОННЫЙ вызов; принудительно сбрасывает
                    // буфер stdout в pipe немедленно, не дожидаясь заполнения
                    // буфера (важно для своевременной доставки данных)

      sleep(1);  // sleep(1) — БЛОКИРУЮЩИЙ вызов; приостанавливает родительский
                 // процесс на 1 секунду; имитирует задержку между отправками
                 // (асинхронность на уровне времени)
    }

    close(STDOUT_FILENO);  // Закрываем stdout (= write-конец pipe); дочерний
                           // процесс получит EOF и завершит цикл scanf

    wait(NULL);  // wait(NULL) — БЛОКИРУЮЩИЙ СИНХРОННЫЙ вызов; родитель ждёт
                 // завершения дочернего процесса; NULL означает, что статус
                 // завершения нас не интересует; предотвращает появление
                 // "зомби-процесса"
  }
}

// ─────────────────────────────────────────────────────────────────────────────
// main() — точка входа программы.
// Просто запускает spawn_and_redirect() и завершается.
// ─────────────────────────────────────────────────────────────────────────────
int main() {
  spawn_and_redirect();  // Запускаем всю логику: создание pipe, fork,
                         // перенаправление, обмен данными
  return 0;  // Возвращаем 0 (успех) операционной системе
}