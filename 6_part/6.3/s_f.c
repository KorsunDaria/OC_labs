#include <stdio.h>       // printf, perror — стандартный ввод/вывод
#include <stdlib.h>      // exit, EXIT_FAILURE — управление процессом
#include <string.h>      // memset, strncpy — работа с памятью и строками
#include <sys/socket.h>  // socket, bind, listen, accept — API сокетов
#include <sys/un.h>      // struct sockaddr_un — структура Unix-сокета
#include <unistd.h>      // read, write, close, fork, unlink — POSIX-вызовы

// Путь к файлу сокета в файловой системе (вместо IP:port)
#define SOCKET_PATH "/tmp/echo_sock"

// Размер буфера для чтения данных от клиента (256 байт)
#define BUFFER_SIZE 256

int main() {
  int server_fd, client_fd;
  // server_fd — дескриптор слушающего сокета (один на весь сервер)
  // client_fd — дескриптор конкретного клиентского подключения

  struct sockaddr_un addr;
  // sockaddr_un — структура адреса для Unix-сокетов:
  //   .sun_family — тип (AF_UNIX)
  //   .sun_path   — путь к файлу сокета

  char buffer[BUFFER_SIZE];
  // Буфер для временного хранения принятых байт от клиента

  // ── 1. СОЗДАНИЕ СОКЕТА ───────────────────────────────────────────────
  // socket(domain, type, protocol)
  //   AF_UNIX      — локальный сокет (через файл, не по сети)
  //   SOCK_STREAM  — поточный: надёжная доставка, порядок гарантирован
  //   (TCP-аналог) 0            — протокол выбирается автоматически
  // Возвращает файловый дескриптор или -1 при ошибке
  if ((server_fd = socket(AF_UNIX, SOCK_STREAM, 0)) == -1) {
    perror("socket error");  // perror выводит системное сообщение об ошибке
    exit(EXIT_FAILURE);      // завершаем процесс с кодом ошибки
  }

  // ── ПОДГОТОВКА АДРЕСА ────────────────────────────────────────────────
  // memset(ptr, value, size) — заполняет всю структуру нулями
  // Обязательно: без этого в sun_path могут остаться мусорные байты
  memset(&addr, 0, sizeof(addr));

  addr.sun_family = AF_UNIX;
  // Указываем тип адреса — Unix-сокет (не inet, не ipv6)

  // strncpy(dst, src, n) — копирует путь в sun_path с ограничением по длине
  // «-1» оставляет место для нуль-терминатора \0
  strncpy(addr.sun_path, SOCKET_PATH, sizeof(addr.sun_path) - 1);

  // unlink(path) — удаляет файл сокета если он остался от прошлого запуска
  // Без этого bind() вернёт EADDRINUSE («адрес уже используется»)
  unlink(SOCKET_PATH);

  // ── 2. ПРИВЯЗКА СОКЕТА ───────────────────────────────────────────────
  // bind(fd, addr, addrlen) — связывает сокет с адресом (создаёт файл)
  //   (struct sockaddr*) — приведение типа: bind принимает общий тип адреса
  //   sizeof(addr)       — размер конкретной структуры sockaddr_un
  if (bind(server_fd, (struct sockaddr*)&addr, sizeof(addr)) == -1) {
    perror("bind error");
    exit(EXIT_FAILURE);
  }

  // ── 3. ПЕРЕВОД В РЕЖИМ ОЖИДАНИЯ ──────────────────────────────────────
  // listen(fd, backlog) — сокет начинает принимать входящие подключения
  //   backlog=5 — максимальная длина очереди ожидающих клиентов
  // После этого вызова сокет становится «пассивным» (только принимает)
  if (listen(server_fd, 5) == -1) {
    perror("listen error");
    exit(EXIT_FAILURE);
  }

  printf("Сервер запущен. Ожидание клиентов...\n");

  // ── ГЛАВНЫЙ ЦИКЛ ─────────────────────────────────────────────────────
  while (1) {
    // accept(fd, addr, addrlen) — БЛОКИРУЕТ процесс до прихода клиента
    //   NULL, NULL — нас не интересует адрес клиента (у Unix-сокетов его нет)
    // Возвращает НОВЫЙ дескриптор для общения с этим конкретным клиентом
    // server_fd остаётся свободным — сразу может принять следующего
    if ((client_fd = accept(server_fd, NULL, NULL)) == -1) {
      perror("accept error");
      continue;  // не падаем, пробуем принять следующего клиента
    }

    // ── 4. СОЗДАНИЕ ДОЧЕРНЕГО ПРОЦЕССА ───────────────────────────────
    // fork() — клонирует текущий процесс:
    //   > 0 — мы в РОДИТЕЛЕ, значение = PID дочернего процесса
    //   = 0 — мы в ДОЧЕРНЕМ процессе
    //   < 0 — ошибка
    if (fork() == 0) {
      // ── ДОЧЕРНИЙ ПРОЦЕСС ─────────────────────────────────────────

      close(server_fd);
      // Закрываем слушающий сокет — дочернему он не нужен.
      // Важно: в памяти дочернего осталась его копия (fork дублирует
      // все дескрипторы), поэтому явно закрываем, чтобы не утечь ресурс

      int bytes_read;

      // Цикл чтения: читаем пока клиент не закроет соединение
      // read(fd, buf, count) — читает до count байт из дескриптора
      //   Возвращает: >0 — число прочитанных байт, 0 — клиент закрыл, -1 —
      //   ошибка
      while ((bytes_read = read(client_fd, buffer, sizeof(buffer))) > 0) {
        // %.*s — форматирование строки заданной длины (без \0 в конце)
        //   bytes_read задаёт точное количество выводимых символов
        printf("Получено и отправлено назад: %.*s", bytes_read, buffer);

        // write(fd, buf, count) — отправляет bytes_read байт клиенту
        // Это и есть «эхо» — возвращаем ровно то, что получили
        write(client_fd, buffer, bytes_read);
      }

      close(client_fd);
      // Закрываем клиентский сокет — сигнализируем клиенту об окончании

      exit(0);
      // Завершаем ДОЧЕРНИЙ процесс нормально.
      // Без exit(0) дочерний продолжил бы выполнение while(1)
      // и начал бы принимать новых клиентов наравне с родителем!
    }

    // ── РОДИТЕЛЬСКИЙ ПРОЦЕСС (fork() > 0) ────────────────────────────
    close(client_fd);
    // Закрываем дескриптор клиента в родителе — он отдан дочернему.
    // Если не закрыть, дескриптор «подвиснет»: при выходе дочернего
    // соединение не закроется, пока родитель держит свою копию.
    // Далее родитель сразу возвращается в начало while(1) → accept()
  }

  return 0;  // Сюда не доберёмся (while(1)), но компилятор требует return
}
